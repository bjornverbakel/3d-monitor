                    <p>
                        I did some testing for the button controls on the 3D-environment. I put all the questions
                        inside a <a href="https://forms.gle/2PU1EnZxhKbmjEsTA" target="_blank" rel="noopener">survey</a>
                        I asked about
                        the position, hover styling and interaction of the buttons. Even though the questions were put
                        into a survey, I still asked them in person so that I could also ask the reasoning for certain
                        answers. I asked various teachers and also a few students.
                    </p>
                    <br>
                    <p>
                        The results were that people prefered to have the button controls in the top left corner because
                        this is where they would look first. Some people prefered to have them in the top right though,
                        because this is where things like maximize/minize buttons would usually be. For the hover effect
                        the inverted colors and border were
                        chosen, as this was the most clearly visible. Someone also suggested having this same inverting
                        of colors, but with the default being white and the hover black. For the small buttons it was
                        chosen to have both
                        on screen at all times instead of swapping them, as this could become confusing.

                    <p>
                        During the UX classes I learned about the dimensions of interaction design. I kept these
                        principles in mind during my design and development process to improve the interactivity and
                        usabilty of the current Fontys OvP website.
                        <br><br>
                        Some examples of where these dimensions have been applied are as followed:
                        <br><br>
                    </p>
                    <ul class="list">
                        <li><span class="bold">Words:</span></li>
                        <p>The text in buttons has been kept clear by labeling them with
                            their exact action, while also being kept short to not overwhelm the user.</p>
                        <li><span class="bold">Visual representations:</span></li>
                        <p>Various icons have been applied to make the functions of the different elements and buttons
                            more clear. For example: carot-icons for dropdowns, three lines for the burger menu icon, a
                            magnifying glass for the search and a flag on the language button.</p>
                        <li><span class="bold">Physical objects or space:</span></li>
                        <p>The website is most likely to be viewed on a desktop, and so the website was designed to be
                            desktop-first</p>
                        <li><span class="bold">Time:</span></li>
                        <p>Various transitions and animations have been applied to give the interactions a smoother
                            feel. This can be seen in elements like the sliding in of menus, hover on header buttons,
                            opening and closing of dropdowns and expanding of search input unpon click.</p>

                            Museum</a> and the <a href="https://web.archive.org/" target="_blank" rel="noopener">Wayback Machine</a> to
                        get a clearer
                        view of this. Additionally, I also looked at a
                        couple of articles and video's that listed some key characteristics of these websites. They
                        often featured things like cluttered or animated background, animated GIFs. Sometimes there
                        would be very big images or icons used as buttons. Another notable characteristic was the way
                        navigation menus were made. I saw a lot of vertical lists, containing a bunch of unstyled
                        hyperlinks.
                    </p>

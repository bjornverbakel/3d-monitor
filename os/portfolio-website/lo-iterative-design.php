                    <h3 id="competitive-analysis">Competitive Analysis</h3>
                    <p>
                        To determine if there is a niche or unique selling point that the competitors are not filling
                        and see what they are doing effectively, I conducted a competitive analysis. I did this by
                        analyzing the websites of studies that offer the most similar courses to that of Fontys Pro.
                    </p>
                    <br>
                    <p>
                        <a href="https://www.avans.nl/studeren/opleidingen/informatica-associate-degree/deeltijd">The
                            Avans informatica AD</a> website immediately starts with a brief overview of all the
                        important
                        information about the study. In terms of videos, they have several interviews with students
                        explaining why they chose the study. Furthermore, there is a video with a tour of the school.
                        For Avans part-time, there is only a link to their Facebook page on the website. Lastly, there
                        is also an informative video, but this needs to be requested and requires filling out a whole
                        list of personal details.
                    </p>
                    <br>
                    <p>
                        <a href="https://www.hva.nl/opleidingen/hbo-ict-deeltijd">The Hogeschool van Amsterdam</a> also
                        has an interview video with a student. Notably, there is a
                        tour where you can look around the school yourself by clicking to navigate and look around 360˚,
                        comparable to something like Google Earth. You can also view it in virtual reality.
                    </p>
                    <br>
                    <p>
                        <a href="https://www.han.nl/opleidingen/hbo/hbo-ict/deeltijd/">HAN</a> has a video with a
                        PowerPoint presentation about the study. However, this video is rather
                        lengthy. Additionally, you can request an advisory meeting and brochure.
                    </p>
                    <br>
                    <p>
                        In summary, the following components often appear:
                    </p>
                    <br>
                    <ul class="list">
                        <li>Open days</li>
                        <li>Orientation days</li>
                        <li>Brochures</li>
                        <li>Social Media Accounts</li>
                        <li>Virtual tours (Videos or interactive)</li>
                        <li>Student interviews</li>
                        <li>Informational videos</li>
                    </ul>
                    <p>
                        <br>
                        Overall, there are not many studies that specifically market to the target audience of people
                        that are already in the work field, and that want to broaden their knowledge and skills. The
                        benefits that Fontys Pro has to them over a normal study could be used to separate them from the
                        competitors.
                    </p>
                </div>
                <div>
                    <h3 id="interviews">Target Audience Interviews</h3>
                    <p>
                        Some interviews with students at Fontys TQ have been conducted, with the aim of better
                        empathizing with our target audience and understanding their needs. Below is a short summary of
                        the findings from the interviews.
                    </p>
                    <br>
                    <p>
                        I interviewed 2 individuals who are enrolled in the part-time evening study program. They were a
                        man (23) and a woman (20). They were already working in the field of ICT and wanted to further
                        expand their knowledge in this area. Part-time study was ideal for them because they were
                        already working full-time. One of the individuals had heard about the evening study program
                        through a friend, while the other was hesitant but had heard negative things about another
                        school. One of them found the website difficult to navigate, while the other had no issues with
                        it. The assignments were not related to their work. Many people use LinkedIn there. The reasons
                        for choosing the part-time evening study program were because a full-time study program did not
                        suit them, and they had more time to work full-time. However, it also has its drawbacks because
                        you do not receive student travel products, and you are not entitled to a loan. This is because
                        you already have an income. Overall, when they were looking for a place to study, they mostly
                        just utilized the website and did not pay much mind to advertisements on social media.
                    </p>

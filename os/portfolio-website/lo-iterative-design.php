                    <p>
                        Furthermore, I noticed these websites were often very cluttered with content, and there was
                        little white space, which I think was one of the biggest factors that made them so unreadable.
                        The last thing I saw a few times was skeuomorphic design, where the elements are styled to
                        resemble real-world objects or materials using things like detailed backgrounds, textures, and
                        shadows.
                    </p>
                    <br>
                    <p>
                        Something that has less to do with design, but more with the practices of then were that
                        personal websites would often feature some kind of hit counter and guestbook. In a way these hit
                        counters would function as a primitive version of Google Analytics, and people would be able to
                        leave a comment on the guestbook about what they thought of the website.
